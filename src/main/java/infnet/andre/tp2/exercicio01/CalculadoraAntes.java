package infnet.andre.tp2.exercicio01;

/**
 * Código original com nomes de variáveis e métodos indecifráveis.
 * Este código apresenta problemas de legibilidade devido à má nomeação.
 *
 * Problemas identificados:
 * - Nome de classe genérico e sem significado (A)
 * - Nome de método abstrato e incompreensível (a)
 * - Parâmetros com nomes de uma única letra sem contexto (x, y)
 * - Variável intermediária sem propósito claro (z)
 *
 * @author André Becker
 * @deprecated Use {@link CalculadoraDepois} para código refatorado
 */
@Deprecated
public class CalculadoraAntes {

    /**
     * Método com nome incompreensível que soma dois números e multiplica por dois.
     *
     * @param x primeiro número
     * @param y segundo número
     * @return resultado da operação
     */
    public static int a(int x, int y) {
        int z = x + y;
        return z * 2;
    }
}
