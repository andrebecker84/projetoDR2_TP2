package infnet.andre.tp2.exercicio06;

/**
 * Enumeração que representa as nacionalidades suportadas pelo sistema.
 *
 * <p>Cada nacionalidade possui cores específicas associadas para
 * personalização da interface do usuário.</p>
 *
 * @author André Becker
 * @version 1.0.0
 */
public enum Nationality {
    /**
     * Nacionalidade holandesa.
     * Cores da bandeira: vermelho, branco e azul.
     */
    DUTCH,

    /**
     * Nacionalidade alemã.
     * Cores da bandeira: preto, vermelho e amarelo.
     */
    GERMAN,

    /**
     * Nacionalidade belga.
     * Cores da bandeira: preto, amarelo e vermelho.
     */
    BELGIAN,

    /**
     * Nacionalidade francesa.
     * Cores da bandeira: azul, branco e vermelho.
     */
    FRENCH,

    /**
     * Nacionalidade italiana.
     * Cores da bandeira: verde, branco e vermelho.
     */
    ITALIAN,

    /**
     * Nacionalidade não classificada.
     * Cores padrão: cinza.
     */
    UNCLASSIFIED
}
