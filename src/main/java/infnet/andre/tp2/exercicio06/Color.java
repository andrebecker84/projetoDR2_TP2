package infnet.andre.tp2.exercicio06;

/**
 * Enumeração que representa as cores disponíveis para personalização.
 *
 * <p>Cores utilizadas para customizar a interface com base na nacionalidade
 * do cliente, refletindo as cores de suas respectivas bandeiras.</p>
 *
 * @author André Becker
 * @version 1.0.0
 */
public enum Color {
    /**
     * Cor vermelha.
     */
    RED,

    /**
     * Cor branca.
     */
    WHITE,

    /**
     * Cor azul.
     */
    BLUE,

    /**
     * Cor preta.
     */
    BLACK,

    /**
     * Cor amarela.
     */
    YELLOW,

    /**
     * Cor verde.
     */
    GREEN,

    /**
     * Cor cinza (padrão).
     */
    GRAY
}
